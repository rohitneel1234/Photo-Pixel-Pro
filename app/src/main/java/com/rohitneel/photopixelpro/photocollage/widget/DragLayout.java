    // ScaleGestureDetector

    @Override
    public boolean onScaleBegin(ScaleGestureDetector scaleDetector) {
        Log.i(TAG, "onScaleBegin");
        return true;
    }

    @Override
    public boolean onScale(ScaleGestureDetector scaleDetector) {
        float scaleFactor = scaleDetector.getScaleFactor();
        Log.i(TAG, "onScale" + scaleFactor);
        if (lastScaleFactor == 0 || (Math.signum(scaleFactor) == Math.signum(lastScaleFactor))) {
            scale *= scaleFactor;
            scale = Math.max(MIN_ZOOM, Math.min(scale, MAX_ZOOM));
            lastScaleFactor = scaleFactor;
        } else {
            lastScaleFactor = 0;
        }

        return true;
    }

    @Override
    public void onScaleEnd(ScaleGestureDetector scaleDetector) {
        Log.i(TAG, "onScaleEnd");
    }

    public void applyScaleAndTranslation() {
        child().setScaleX(scale);
        child().setScaleY(scale);
        child().setTranslationX(dx);
        child().setTranslationY(dy);
    }
    public void applyScaleAndTranslationVertical() {
        child().setScaleX(scale);
        child().setScaleY(scale);
        child().setTranslationX(dx);
        child().setTranslationY(dy);
    }
    public void applyScaleAndTranslation(float scalex,float scaley,float dx,float dy) {
        child().setScaleX(scalex);
        child().setScaleY(scaley);
        child().setTranslationX(dx);
        child().setTranslationY(dy);
    }
    public void applyScaleAndTranslationVertical(float scale,float dx,float dy) {
        child().setScaleX(-scale);

        child().setScaleY(scale);
        child().setTranslationX(dx);
        child().setTranslationY(dy);
    }
    private View child() {
        return getChildAt(0);
    }

